package com.marte;

public class Planalto {

    private Integer eixox;
    private Integer eixoy;

    public Planalto(Integer eixox, Integer eixoy) {
        this.eixox = eixox;
        this.eixoy = eixoy;
    }

    public Integer getEixox() {
        return eixox;
    }

    public Integer getEixoy() {
        return eixoy;
    }

}
